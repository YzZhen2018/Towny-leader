package com.suixin.townyleader;

public class MayorBetDate {
    private String townyName;//城镇名字
    private String nation;//所属国家
    private String mayor;//领导人名字
    private String residents;//城镇居民

    public String getTownyName() {
        return townyName;
    }

    public void setTownyName(String townyName) {
        this.townyName = townyName;
    }

    public String getMayor() {
        return mayor;
    }

    public void setMayor(String mayor) {
        this.mayor = mayor;
    }

    public String getNation() {
        return nation;
    }

    public void setNation(String nation) {
        this.nation = nation;
    }

    public String getResidents() {
        return residents;
    }

    public void setResidents(String residents) {
        this.residents = residents;
    }
}
